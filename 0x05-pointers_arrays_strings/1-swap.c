/**                                                                              *swap_int - takes a pointer to an int as parameter                              * @a: parameter take the value.  
 * @b: parameter take the value.                                                 */

void swap_int(int *a, int *b)
{
	int c = *a;

	*a = *b;
	*b = c;
}

                                                                                                                                


		                                                                                                                                

